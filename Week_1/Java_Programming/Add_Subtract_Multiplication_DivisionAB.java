import java.util.Scanner;

public class Add_Subtract_Multiplication_DivisionAB {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        System.out.println((a+b)+" "+(a-b)+" "+(a*b)+" "+(a/b));
    }
}
